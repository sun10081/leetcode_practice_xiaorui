    def mergeTwoLists(self, l1: ListNode, l2: ListNode) -> ListNode:
        """
        recursion 重点看下 第二遍做没做出来
        :param l1:
        :param l2:
        :return:
        """
        if not l1:
            return l2
        elif not l2:
            return l1
        elif l1.val < l2.val:
            l1.next = self.mergeTwoLists(l1.next, l2)
            return l1
        else:
            l2.next = self.mergeTwoLists(l1, l2.next)
            return l2

    def mergeTwoLists2(self, l1: ListNode, l2: ListNode) -> ListNode:
        """
        iteration
        :param l1:
        :param l2:
        :return:
        """
        pre_head = pre = ListNode(-1)
        while l1 and l2:
            if l1.val < l2.val:
                pre.next = l1
                l1 = l1.next
            else:
                pre.next = l2
                l2 = l2.next
            pre = pre.next

        pre.next = l1 if l1 else l2
        return pre_head.next


